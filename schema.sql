create table person(
    id int auto_increment,
    name VARCHAR(255) null,
    address VARCHAR(255) null,
    email VARCHAR(255) null,
    phone VARCHAR(255) null,

    CONSTRAINT pk_person_id PRIMARY KEY (id)
);

